# practice for functions!

def meal(A,B,C):
    print(f"There're {A} kinds of milk.")
    print(f"And {B} kinds of chocolate.")
    print(f"And {C} kinds of vegetables.")
    print(f"That's {A + B + C} types together, we can open a supermarker!")

meal(1,3,4)

meal(2-3,12-8,2*14)

x = 1234
y = 12
z = 4
meal(x,y,z)
